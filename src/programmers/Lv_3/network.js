const solution = (n, computers) => {
  let answer = 0;
  let dx = [-1, 0, 1, 0]; // ìƒìš°í•˜ì¢Œ
  let dy = [0, 1, 0, -1];

  const dfs = (x, y) => {
    computers[x][y] = 0; // ë°©ë¬¸ í‘œì‹œ
    for (let k = 0; k < 4; k++) {
      let nx = x + dx[k];
      let ny = y + dy[k];
      if (nx >= 0 && nx < n && ny >= 0 && ny < n && computers[nx][ny] === 1) {
        dfs(nx, ny);
      }
    }
  };

  for (let i = 0; i < n; i++) {
    for (let j = 0; j < n; j++) {
      if (computers[i][j] === 1) {
        answer++;
        dfs(i, j);
      }
    }
  }

  return answer;
};
