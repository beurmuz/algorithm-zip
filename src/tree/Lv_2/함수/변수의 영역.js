let [a, b] = require("fs")
  .readFileSync("/dev/stdin")
  .toString()
  .trim()
  .split("\n");

function matchStr() {
    for(let i = 0; i < a.length; i++) {
        let cnt = 0;
        if(a[i] === b[0]) {
            for(let j = 0; j < b.length; j++) {
                if(a[i + j] === b[j]) cnt++;
            }
            if(cnt === b.length) {
                return i;
            }
        }
    }
    return -1;
}

